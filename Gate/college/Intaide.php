<h1>Présentation INT’Aide</h1>

<p>INT’Aide est une association d’étudiants des écoles Telecom & Management SudParis (regroupement d’une école de commerce et d’une école d’ingénieurs). Ces étudiants viennent chaque Lundi et Jeudi de 17h à 18h donner des cours de soutien bénévole aux élèves de 3ème du collège qui le souhaitent, le tout dans une ambiance conviviale.</p>

<p>Mais INT’Aide ce n’est pas que ça ! D’autres activités sont organisées en parallèle, comme la sortie pour la pièce de théâtre organisée par le Club Théâtre de Telecom & Management SudParis , ou encore des visites prévues sur Paris...</p>

<p>En un mot, INT’Aide ce sont des étudiants motivés qui veulent apporter un peu de leur temps pour aider de jeunes collégiens aussi bien dans leur parcours scolaire que dans leur épanouissement culturel et personnel.</p>
