<p>
<h2>Qui sommes-nous ?</h2>
Un groupe de 7 étudiants de l’école d’ingénieurs Télécom Sud Paris, à 5 minutes à pied du collège Paul Eluard. Nous travaillons dans le cadre d’un projet de DCP : Développement de Compétences Professionnelles. 
</p>

<p>
<h2>Quel est notre objectif ?</h2>
Montrer aux élèves que les sciences sont présentes au quotidien, et que l’on peut s’amuser en faisant des expériences.
</p>

<p>
<h2>Qui bénéficie du projet ?</h2>
20 élèves en classe de 3ème au collège Paul Eluard, choisis parmi des volontaires.
<p>

<p>
<h2>Que faisons-nous, et qu’allons nous faire ?</h2>
<ul>
<li>Nous avons réalisé avec les élèves l’affiche du projet, qui sera affichée dans la salle de classe des élèves, et sur ce site Internet dès que nous l’aurons numérisée.</li>

<li>Nous avons organisé une première demi-journée d’expériences durant laquelle nous avons fabriqué des boussoles, montré aux élèves un radiomètre de Crooks, parlé des phénomènes d’irisation sur une bulle de savon, et avons illustré le fonctionnement des châteaux d’eau.</li>

<li>Nous prévoyons d’emmener les élèves à la cité des sciences (sortie gratuite pour les élèves).</li>

<li>Nous sommes en train d’organiser une seconde demi-journée d’expériences, durant laquelle nous illustrerons le phénomène des éruptions volcaniques grâce à une expérience chimique, nous construirons une pile avec un citron, expliquerons grâce à un détecteur de PH naturel cuisiné avec de la betterave les notions d’acidité et de basicité, montreront la vaporisation de fumigènes, etc. … Sous réserve de parvenir à nous procurer de la carboglace, nous leur expliqueront les changements d’états.</li>
</ul>
</p>

